print ("โปรแกรมคำนวณค่าน้ำประปา (ต่อลูกบาศก์เมตร)")

water = int(input("ใช้น้ำเดือนนี้เท่าไหร่ (ลูกบาศก์เมตร) : "))

if water <= 4:
    print("ฟรี ครับ")
elif water <= 10:
    print("ค่าน้ำราคาเดือนนี้ : ",(water) * 8, "บาทครับ")
elif water <= 20:
    print("ค่าน้ำราคาเดือนนี้ : ",(water) * 12, "บาทครับ")
else:
    print("ค่าน้ำราคาเดือนนี้ : ",(water) * 15, "บาทครับ")


